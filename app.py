# VERIFY QR
# ===============================
def verify_qr(uploaded_image):

    if uploaded_image is None:
        return "No image uploaded ❌", "", "", ""

    img = cv2.cvtColor(np.array(uploaded_image), cv2.COLOR_RGB2BGR)
    detector = cv2.QRCodeDetector()
    data, _, _ = detector.detectAndDecode(img)

    if not data:
        return "QR Code not detected ❌", "", "", ""

    try:
        parts = data.split("|SIG:")
        encrypted_str = parts[0].replace("ENC_DATA:", "")
        signature_hex = parts[1]

        encrypted_bytes = encrypted_str.encode()
        signature = bytes.fromhex(signature_hex)

        public_key.verify(
            signature,
            encrypted_bytes,
            padding.PSS(
                mgf=padding.MGF1(hashes.SHA256()),
                salt_length=padding.PSS.MAX_LENGTH
            ),
            hashes.SHA256()
        )

        decrypted = fernet.decrypt(encrypted_bytes).decode()
        transaction = json.loads(decrypted)

        risk = assess_risk(transaction)
        tx_id = transaction["TransactionID"]

        if risk == "High":
            return "✅ Signature Valid", decrypted, "HIGH RISK", "🚫 BLOCKED"

        elif risk == "Medium":
            otp_store[tx_id] = str(random.randint(100000, 999999))
            return (
                "✅ Signature Valid",
                decrypted,
                "MEDIUM RISK",
                f"⚠ OTP Required (Demo OTP: {otp_store[tx_id]})"
            )

        else:
            return "✅ Signature Valid", decrypted, "LOW RISK", "✅ APPROVED"

    except Exception:
        return "❌ Signature Verification FAILED", "", "", "🚫 STOPPED"

# ===============================
# OTP VERIFY
# ===============================
def verify_otp(tx_id, user_input):

    if tx_id not in otp_store:
        return "No OTP required or expired."

    if user_input == otp_store[tx_id]:
        del otp_store[tx_id]
        return "✅ OTP Correct — Transaction Approved"

    return "❌ Incorrect OTP — Transaction Blocked"

