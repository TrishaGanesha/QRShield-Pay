otp_store = {}

# ===============================
# LOGGING
# ===============================
def log_transaction(data):
    with open("transactions.log", "a") as f:
        f.write(json.dumps(data) + "\n")

# ===============================
# RISK ANALYSIS
# ===============================
def assess_risk(transaction):
    score = 0

    if transaction["Amount"] > 2000:
        score += 3
    elif transaction["Amount"] > 500:
        score += 2
    else:
        score += 1

    if score >= 3:
        return "High"
    elif score == 2:
        return "Medium"
    else:
        return "Low"

# ===============================
# GENERATE QR
# ===============================
def generate_secure_qr(user_id, amount, currency, merchant_id):

    transaction_id = str(uuid.uuid4())

    transaction = {
        "TransactionID": transaction_id,
        "UserID": user_id,
        "Amount": float(amount),
        "Currency": currency,
        "MerchantID": merchant_id,
        "Timestamp": datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    }

    transaction_json = json.dumps(transaction)

    encrypted_data = fernet.encrypt(transaction_json.encode())
    encrypted_str = encrypted_data.decode()

    signature = private_key.sign(
        encrypted_data,
        padding.PSS(
            mgf=padding.MGF1(hashes.SHA256()),
            salt_length=padding.PSS.MAX_LENGTH
        ),
        hashes.SHA256()
    )

    payload = f"ENC_DATA:{encrypted_str}|SIG:{signature.hex()}"

    qr = qrcode.QRCode(box_size=10, border=4)
    qr.add_data(payload)
    qr.make(fit=True)
    img = qr.make_image(fill_color="black", back_color="white")

    log_transaction(transaction)

    return img.convert("RGB"), transaction_json

